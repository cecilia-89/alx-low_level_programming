void main(void)
{
	int op_add(int a, int b)
	{
	        int sum;

	        sum = a + b;

	        return sum;

	}


	int op_sub(int a, int b)
	{
		int diff;

		diff = a - b;

		return diff;

	}

	int op_mul(int a, int b)
	{
	        int product;

		product = a * b;

		return product;

	}

	int op_div(int a, int b)
	{
	        int quotient;

		quotient = a / b;

		return quotient;

	}

	int op_mod(int a, int b)
	{
		int modulo;

		modulo = a % b;

		return modulo;

	}


}
